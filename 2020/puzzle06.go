// This finds how many tree's you hit. This was
// made on playground because my laptop got taken.
package main

import (
	"bufio"
	"fmt"
	"strings"
)

type pos struct {
	x, y int
}

type slope struct {
	rise, run int
}

var slopesToCheck = []slope{
	slope{rise: -1, run: 1},
	slope{rise: -1, run: 3},
	slope{rise: -1, run: 5},
	slope{rise: -1, run: 7},
	slope{rise: -2, run: 1},
}

var interpMap = map[string]bool{
	".": false,
	"#": true,
}

func main() {
	puzzleKey := 1
	for _, slope := range slopesToCheck {
		var numHitTree int
		p := pos{}

		scanner := bufio.NewScanner(strings.NewReader(data))
		scanner.Split(bufio.ScanLines)

		scanner.Scan()
		for {
			splitLine := strings.Split(scanner.Text(), "")
			if interpMap[splitLine[p.x]] {
				numHitTree++
			}

			p.x += slope.run
			if p.x >= len(splitLine) {
				p.x -= len(splitLine)
			}

			for i := 0; i > slope.rise+1; i-- {
				scanner.Scan()
			}

			if ok := scanner.Scan(); !ok {
				break
			}
		}

		puzzleKey *= numHitTree
		fmt.Printf("Number of tree's hit with slope %+v is: %v\n", slope, numHitTree)
	}

	fmt.Printf("Puzzle key is: %v", puzzleKey)
}

