/* With the toboggan login problems resolved, you set off toward
 * the airport. While travel by toboggan might be easy, it's
 * certainly not safe: there's very minimal steering and the area
 * is covered in trees. You'll need to see which angles will take
 * you near the fewest trees.
 *
 * Due to the local geology, trees in this area only grow on exact
 * integer coordinates in a grid. You make a map (your puzzle input)
 * of the open squares (.) and trees (#) you can see.
 */

// This finds and visualizes how many tree's you hit.
// This was made on playground because my laptop got taken.
package main

import (
	"bufio"
	"fmt"
	"strings"
)

type pos struct {
	x, y int
}

type slope struct {
	rise int
	run  int
}

var interpMap = map[string]bool{
	".": false,
	"#": true,
}

func main() {
	s := slope{run: 3}

	var numHitTree int
	p := pos{}

	scanner := bufio.NewScanner(strings.NewReader(data))
	scanner.Split(bufio.ScanLines)

	for {
		if !scanner.Scan() {
			break
		}

		splitLine := strings.Split(scanner.Text(), "")
		if interpMap[splitLine[p.x]] {
			numHitTree++
			splitLine[p.x] = "X"
		} else {
			splitLine[p.x] = "O"
		}

		fmt.Println(strings.Join(splitLine, ""))

		p.x += s.run
		if p.x >= len(splitLine) {
			p.x -= len(splitLine)
		}
	}

	fmt.Printf("Number of tree's hit: %v\n", numHitTree)
}

